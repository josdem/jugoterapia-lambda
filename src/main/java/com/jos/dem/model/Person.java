package com.jos.dem.model;

public class Person {
  private String name;
  private String lastname;

  public void setName(String name){
    this.name = name;
  }

  public String getName(){
    return name;
  }

  public void setLastname(String lastname){
    this.lastname = lastname;
  }

  public String getLastname(){
    return lastname;
  }

}


